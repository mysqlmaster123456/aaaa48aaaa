    public void Restartovat(){
        firePropertyChange("SmazPole", false, true);
        hra.CreateField(hra.x, hra.y);
        hra.Moves = 0;
        hra.konec=false;
        firePropertyChange("NewSize", false, true);
        firePropertyChange("Restart", false, true);
    }
    
    public void Menu(){
        firePropertyChange("SmazPole", false, true);
        hra.CreateField(hra.x, hra.y);
        hra.Moves = 0;
        hra.konec=false;
        firePropertyChange("NewSize", false, true);
        firePropertyChange("BackToMenu", false, true);
    }
    
    public void PocetPohybu(){
        jLabel2.setText("Pocet Tahu: "+(hra.Moves-1));
    }

    
